#include <unistd.h>

int main(int argc, char *argv[])
{
	int n;

	n = 0;
	if (argc != 2)
	{
	write(1, "a\n", 2);
	return (0);
	}
	while (argv[1][n] != '\0')
	{
		if (argv[1][n] == 'a')
		{
		write(1, "a\n", 2);
		return (0);
		}
	n++;
	}
	write(1, "\n", 2);
	return (0);
}