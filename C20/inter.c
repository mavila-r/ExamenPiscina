#include <unistd.h>

int main(int argc, char *argv[])
{
int n1;
int n2;

n1 = 0;
	if (argc == 3)
	{
		while (argv[1])
		{
		n2 = 0;
			while ((argv[2][n2]) == argv[1][n1 + n2] && argv[n1 + n2])
			{
				write(1, &argv[2][n2], 1);
				n2++;
			}
		n1++;
		}
	}
write(1, "\n", 1);
return (0);
}